package javaapplication1;

public class Oddnum {

    public static void main(String[] args) {
       //loop 1 to 100 
        for(int i=1 ; i<=100 ; i++ )
        {
            //condition odd
            if(i%2==1)
                    {
                        System.out.print(" "+i);
                    }
        }
    }
}
