package javaapplication1;

public class Evennum {

    public static void main(String[] args) {
        //loop to get num from 1 to 100
        for(int i=1 ; i<=100 ; i++)
        {
            //even condition
            if(i%2==0)
            {
                System.out.print(" "+i);
            }
        }
    }
}
